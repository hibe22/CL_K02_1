#include <stdio.h>
void main(void){
	int a,b,c;
	printf("aの値を入力してください：");
	scanf("%d",&a);
	printf("bの値を入力してください：");
	scanf("%d",&b);
	printf("cの値を入力してください：");
	scanf("%d",&c);
	a = a + b * c;
	printf("aとbとcの積和演算はは%dです。\n",a);
}